import random

# (variable) randint: (a: int, b: int) -> int
# Return random integer in range [a, b], including both end points

random_int = random.randint(1,10)
print(random_int)

# (variable) random: () -> float
# random() -> x in the interval [0, 1)

random_float = random.random()
print(random_float)

